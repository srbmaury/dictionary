var ans="";

function Show(){
    var original =document.getElementById("inputs").value;
    var x = original.trim();
    var y=x.toLowerCase();
    if(y.length === 0){
        document.getElementById("ex").innerHTML = "";
        document.getElementById("demo").innerHTML = "Please enter a word";
    }
    else if(dict.hasOwnProperty(y))
    {
        document.getElementById("ex").innerHTML = "The hindi meaning of " + x + " is:";
        document.getElementById("demo").innerHTML = dict[y];
    }
    else{
        document.getElementById("ex").innerHTML = "";
        document.getElementById("demo").innerHTML = "match not found!";
    }
}
function ShoWords(){
    for(x in dict){
        let ansi = x.toUpperCase().charAt(0) + x.substring(1);
        let len = ansi.length;
        
        while(len != 20){
            ansi +="&nbsp";
            len++;
        }
        ans+=((ansi+":&nbsp&nbsp&nbsp&nbsp" + dict[x].trim())+"<br>");
    }
    document.getElementById('refresh').style.visibility='visible';
    var mainpage = document.getElementById('mainpage');
    mainpage.style.height = '0';
    mainpage.style.width = '0';
    var com = document.getElementById('com');
    com.style.visibility = "visible";
    com.style.height="80vh";
    mainpage.style.visibility='hidden';
    com.innerHTML = ans;
}